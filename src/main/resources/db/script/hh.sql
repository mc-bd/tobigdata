SELECT
  (MONTARGET*COUNT) CALC_MC,
  STARTDATE,
  FINISHDATE,
  MONTARGET,
  NOWTARGET ACHIEVEMENT
FROM
  BD_GOAL
WHERE
  PK_GOAL=1;

